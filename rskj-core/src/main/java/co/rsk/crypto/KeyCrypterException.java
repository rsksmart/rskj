package co.rsk.crypto;

/**
 * Exception to provide the following:
 *
 * <ul>
 *   <li>Provision of encryption / decryption exception
 * </ul>
 *
 * <p>This base exception acts as a general failure mode not attributable to a specific cause (other
 * than that reported in the exception message). Since this is in English, it may not be worth
 * reporting directly to the user other than as part of a "general failure to parse" response.
 */
public class KeyCrypterException extends RuntimeException {
    private static final long serialVersionUID = -4441989608332681377L;

    public KeyCrypterException(String s) {
        super(s);
    }

    public KeyCrypterException(String s, Throwable throwable) {
        super(s, throwable);
    }
}
